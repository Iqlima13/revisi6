<p>Badan Penanggulangan Bencana Daerah (BPBD) Kabupaten Tasikmalaya merupakan
Organisasi Perangkat Daerah (OPD) yang menaungi urusan penanggulangan bencana baik
sebelum terjadi bencana, pada saat bencana dan sesudah bencana. Dibentuk melalui Peraturan
Daerah Kabupaten Tasikmalaya Nomor 14 Tahun 2011 tentang Perubahan Atas Peraturan Daerah
Kabupaten Tasikmalaya Nomor 15 Tahun 2008 tentang Dinas Daerah Kabupaten Tasikmalaya dan
Peraturan Daerah Kabupaten Tasikmalaya Nomor 15 Tahun 2011 tentang Badan Penanggulangan
Bencana Daerah.
Dalam perjalanannya melalui Peda 3 Tahun 2021 tentang Struktur Organisasi Tata Kerja pada
Pemerintah Daerah Kabupaten Tasikmalaya, UPTD Pemadam Kebakaran bergabung menjadi
bagian dari BPBD Kabupaten Tasikmalaya dengan pertimbangan kesamaan rumpun urusan, yakni
urusan kebencanaan.</p>
